/**
 * Get translation — falls back to original if not found.
 * Also does pattern matching for World Cup, NBA, NFL, etc.
 */
export function t(key) {
  if (!key) return '';
  // Exact match
  if (translations[key]) return translations[key];

  // Fuzzy: FIFA World Cup questions
  const fifaMatch = key.match(/^Will (.+?) win the 2026 FIFA World Cup\?$/);
  if (fifaMatch) {
    return `2026 FIFA Dünya Kupası'nı ${fifaMatch[1]} kazanacak mı?`;
  }

  // Pattern: "Will [team] win the 2026 NBA Finals?"
  const nbaMatch = key.match(/^Will (?:the )?(.+?) win the 2026 NBA Finals\?$/);
  if (nbaMatch) {
    return `${nbaMatch[1]} 2026 NBA Finallerini kazanacak mı?`;
  }

  // Pattern: "Will [team] win the 2026 Super Bowl?" / NFL
  const nflMatch = key.match(/^Will (.+?) win the (2026 )?Super Bowl\?$/i);
  if (nflMatch) {
    return `${nflMatch[1]} ${nflMatch[2] || ''}Super Bowl'ı kazanacak mı?`;
  }

  // Pattern: "Will [team] win the 2026 Stanley Cup?"
  const nhlMatch = key.match(/^Will (.+?) win the 2026 Stanley Cup\?$/);
  if (nhlMatch) {
    return `2026 Stanley Kupası'nı ${nhlMatch[1]} kazanacak mı?`;
  }

  // Pattern: "Will [player/team] win the 2026 [Grand Slam/Tournament]?"
  const winMatch = key.match(/^Will (?:the )?(.+?) win the 2026 (.+)\?$/);
  if (winMatch) {
    const subject = winMatch[1];
    const event = winMatch[2];
    // Common event translations
    const eventTrs = {
      'Australian Open': 'Avustralya Açık\'ı',
      'French Open': 'Fransa Açık\'ı',
      'Wimbledon': 'Wimbledon\'ı',
      'US Open': 'ABD Açık\'ı',
      'Masters Tournament': 'Masters Turnuvası\'nı',
      'The Open Championship': 'Açık Şampiyona\'yı',
      'Masters': 'Masters\'ı',
    };
    const eventTr = eventTrs[event] || event;
    return `${subject} 2026 ${eventTr} kazanacak mı?`;
  }

  // Pattern: "Will [X] happen before [Y]?" (compound markets)
  const beforeMatch = key.match(/^Will (.+?) happen before (.+)\?$/);
  if (beforeMatch) {
    return `${beforeMatch[1]}, ${beforeMatch[2]} olayından önce gerçekleşecek mi?`;
  }

  // Pattern: "Will [X] achieve [Y] by [date]?" (time-bound)
  const achieveMatch = key.match(/^Will (.+?) (?:achieve|hit|reach|surpass) (.+) by (.+)\?$/i);
  if (achieveMatch) {
    return `${achieveMatch[1]}, ${achieveMatch[2]} hedefine ${achieveMatch[3]} tarihine kadar ulaşacak mı?`;
  }

  return key;
}

/**
 * Translate a category name
 */
export function translateCategory(category) {
  if (!category) return 'Kategori';
  return t(category.toLowerCase());
}
